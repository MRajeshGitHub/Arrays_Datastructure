package com.virtusa.core;

public class Array_EvenOdd {

	public static void main(String[] args) {
		
		int[]arr= {27,33,44,55,6,66,77,87,88};
		System.out.println("Print array first element is Even or not");
		
		int first=arr[0];
		
		if(first%2==0) {
			System.out.println("even");
		}
		else {
			System.out.println("odd");
		}
		
		
		
	}
}
